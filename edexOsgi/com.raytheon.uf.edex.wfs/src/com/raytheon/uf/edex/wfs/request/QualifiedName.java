package com.raytheon.uf.edex.wfs.request;

import javax.xml.namespace.QName;

/**
 * @author bclement
 * 
 */
public class QualifiedName {

	protected String namespace;

	protected String name;

	protected String prefix;

	public QualifiedName(String namespace, String name, String prefix) {
		this.namespace = namespace;
		this.name = name;
		this.prefix = prefix;
	}

	public QualifiedName(String namespace, String name) {
		this(namespace, name, null);
	}

    public QualifiedName(QName name) {
        this.namespace = name.getNamespaceURI();
        this.name = name.getLocalPart();
        this.prefix = name.getPrefix();
    }

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		result = prime * result
				+ ((namespace == null) ? 0 : namespace.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		QualifiedName other = (QualifiedName) obj;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		if (namespace == null) {
			if (other.namespace != null)
				return false;
		} else if (!namespace.equals(other.namespace))
			return false;
		return true;
	}

	/**
	 * @return the namespace
	 */
	public String getNamespace() {
		return namespace;
	}

	/**
	 * @param namespace
	 *            the namespace to set
	 */
	public void setNamespace(String namespace) {
		this.namespace = namespace;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the prefix
	 */
	public String getPrefix() {
		return prefix;
	}

	/**
	 * @param prefix
	 *            the prefix to set
	 */
	public void setPrefix(String prefix) {
		this.prefix = prefix;
	}

	@Override
	public String toString() {
		return namespace + ":" + name;
	}

}
